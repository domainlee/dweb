<?php
namespace Admin\Model;

use Base\Model\Base;
class Store extends Base{
	
	protected $id;
	protected $name;
	protected $status;

    /**
     * @return mixed
     */
    public function getId()
    {
        return $this->id;
    }

    /**
     * @param mixed $id
     */
    public function setId($id)
    {
        $this->id = $id;
    }

    /**
     * @return mixed
     */
    public function getName()
    {
        return $this->name;
    }

    /**
     * @param mixed $name
     */
    public function setName($name)
    {
        $this->name = $name;
    }

    /**
     * @return mixed
     */
    public function getStatus()
    {
        return $this->status;
    }

    /**
     * @param mixed $status
     */
    public function setStatus($status)
    {
        $this->status = $status;
    }

    public function toFormValues()
    {
        $data =  array(
            'name' => $this->getName(),
            'status' => $this->getStatus(),
        );

        return $data;
    }


}